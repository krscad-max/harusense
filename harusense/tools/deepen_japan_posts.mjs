let changed = 0;
for (const file of posts){
  const full = join(dir, file);
  const md = readFileSync(full, 'utf8');
  const p = split(md);
  if (!p) continue;

  const { marker, section } = sectionsBySlug[file] ?? {};
  if (!marker || !section) continue;

  let body = p.body;
  body = ensure(body, marker, section);

  writeFileSync(full, p.fm + body.trimStart(), 'utf8');
  changed++;
}

console.log(`Deepened ${changed} Japan travel posts.`);
